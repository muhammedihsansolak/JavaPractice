package coreJavaPractice.day18_Practice;

public class RectangleWithNestedLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }






    }
}
